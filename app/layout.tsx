import type { Metadata } from "next";
import Link from "next/link";
import "./globals.css";
import { cookies } from "next/headers";
import jwt from "jsonwebtoken";
import { logoutUser } from "./actions/authActions";
import DarkMode from "./components/Darkmode";

export const metadata: Metadata = {
  title: "나의 게시판 서비스",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const cookieStore = await cookies();
  const token = cookieStore.get("token")?.value;
  const theme = cookieStore.get("theme")?.value || "light";

  type JwtUser = {
    userId: string;
    nickname: string;
    role: string;
  };

  let user: JwtUser | null = null;

  if (token) {
    try {
      user = jwt.verify(token, process.env.JWT_SECRET || "") as JwtUser;
    } catch {
      user = null;
    }
  }
  return (
    <html lang="ko" className={theme} style={{ colorScheme: theme }}>
      <body className="antialiased font-sans dark:text-white transition-colors duration-300">
        <nav className="flex justify-between items-center p-6 border-b dark:!bg-slate-900 dark:!border-slate-800">
          <div className="flex items-center gap-8">
            <Link
              className="text-3xl font-extrabold tracking-tighter dark:text-white"
              href={"/"}
            >
              HOME
            </Link>
            <Link
              className="text-lg font-medium dark:text-gray-300 dark:hover:text-blue-400"
              href={"/list"}
            >
              List
            </Link>
          </div>

          <div className="flex items-center gap-4">
            <DarkMode currentTheme={theme}></DarkMode>
            {user ? (
              <div className="flex items-center gap-4">
                <span className="text-sm font-semibold dark:text-white">
                  <span className="text-blue-600">{user.nickname}</span>님,
                  반가워요!
                </span>
                <form action={logoutUser}>
                  <button
                    type="submit"
                    className="text-sm font-medium text-red-500 hover:text-red-700 border border-red-200 px-3 py-1.5 rounded-md hover:bg-red-50"
                  >
                    로그아웃
                  </button>
                </form>
              </div>
            ) : (
              <>
                <Link
                  className="text-sm font-medium text-gray-600 hover:text-black border border-gray-300 px-3 py-1.5 rounded-md"
                  href="/login"
                >
                  로그인
                </Link>
                <Link
                  className="text-sm font-medium bg-black text-white px-3 py-1.5 rounded-md hover:bg-gray-800"
                  href="/register"
                >
                  회원가입
                </Link>
              </>
            )}
          </div>
        </nav>
        <main className="mx-auto min-h-screen p-4">{children}</main>
      </body>
    </html>
  );
}
